package simulation.networks;

/** Class to hold a coordinate.
 * @author ykk
 */
public class Coordinate
{
    //Members
    /** Position in x coordinate.
     */
    public double x;
    /** Position in y coordinate.
     */
    public double y;

    //Methods
    /** Constructor
     * @param x x-coordinate
     * @param y y-coordinate
     */
    public Coordinate(double x, double y)
    {
	this.x = x;
	this.y = y;
    }
}
